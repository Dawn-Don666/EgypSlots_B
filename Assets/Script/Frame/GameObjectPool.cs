using System.Collections;
using System.Collections.Generic;
using UnityEngine;

/// <summary>
/// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
/// </summary>
public class GameObjectPool : Youngster<GameObjectPool>
{
    public Dictionary<string, Queue<GameObject>> poolDic = new Dictionary<string, Queue<GameObject>>();

    /// <summary>
    /// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝศก๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    /// </summary>
    /// <param name="name">๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ</param>
    /// <param name="gameObj">๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝิค๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ</param>
    /// <returns></returns>
    public GameObject GetObj(string name, GameObject gameObj)
    {
        GameObject obj = null;
        //๏ฟฝะถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃ๏ฟฝ้ฒข๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃ๏ฟฝ๏ฟฝ๏ฟฝะป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝะถ๏ฟฝ๏ฟฝ๏ฟฝ
        if (poolDic.ContainsKey(name) && poolDic[name].Count > 0)
        {
            //๏ฟฝรต๏ฟฝ๏ฟฝ๏ฟฝ0๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
            obj = poolDic[name].Dequeue();
        }
        else
        {
            //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝรป๏ฟฝะถ๏ฟฝ๏ฟฝ๏ฟฝอด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
            obj = GameObject.Instantiate(gameObj);
            //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮช๏ฟฝอณ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝาป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอฟ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝPushObj๏ฟฝ๏ฟฝ๏ฟฝรถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝึดๆดข
            obj.name = name;
        }
        //๏ฟฝ๏ฟฝ๏ฟฝๅผค๏ฟฝ๎ฃฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสพ
        obj.SetActive(true);
        return obj;
    }

    /// <summary>
    /// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝีปุณ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    /// </summary>
    /// <param name="name"></param>
    /// <param name="obj"></param>
    public void PushObj(GameObject obj)
    {
        //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสง๏ฟฝ๎ฃฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
        obj.SetActive(false);
        //๏ฟฝ๏ฟฝ๏ฟฝรป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃ๏ฟฝ๏ฟฝอด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃ๏ฟฝ๏ฟฝ๏ฟฝูด๏ฟฝ
        if (!poolDic.ContainsKey(obj.name))
        {
            poolDic.Add(obj.name, new Queue<GameObject>());
        }
        poolDic[obj.name].Enqueue(obj);
    }

    /// <summary>
    /// ๏ฟฝ๏ฟฝีป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    /// </summary>
    public void Clear()
    {
        poolDic.Clear();
    }
}
